##lista
myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))

## Acceso a una lista por posición
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

myFruitList[2] = "orange"
print(myFruitList)


